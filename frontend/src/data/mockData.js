// Помощник для получения статуса
export const getStatusInfo = (statusKey) => {
  return Object.values(carStatuses).find(s => s.key === statusKey) || carStatuses.ORDERED;
};

// Помощник для расчета прибыли
export const calculateProfit = (car) => {
  const totalCost = car.purchasePrice + car.shippingCost + car.customsCost + car.repairCost + car.additionalCost;
  return car.sellingPrice - totalCost;
};

// Помощник для расчета общей стоимости
export const calculateTotalCost = (car) => {
  return car.purchasePrice + car.shippingCost + car.customsCost + car.repairCost + car.additionalCost;
};

// Форматирование валюты
export const formatCurrency = (amount, currency = 'USD') => {
  if (currency === 'USD') {
    return new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD' }).format(amount);
  } else if (currency === 'KRW') {
    return new Intl.NumberFormat('ko-KR', { style: 'currency', currency: 'KRW' }).format(amount);
  } else if (currency === 'RUB') {
    return new Intl.NumberFormat('ru-RU', { style: 'currency', currency: 'RUB' }).format(amount);
  }
  return amount;
};

// Форматирование даты
export const formatDate = (dateString) => {
  if (!dateString) return '—';
  return new Date(dateString).toLocaleDateString('ru-RU', {
    day: 'numeric',
    month: 'short',
    year: 'numeric',
  });
};
